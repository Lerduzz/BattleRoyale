    `id` INT NOT NULL AUTO_INCREMENT, 
    `map_id` INT UNSIGNED NOT NULL DEFAULT 0,
    `map_name` VARCHAR(255) NOT NULL,
    `center_x` FLOAT NOT NULL DEFAULT 0,
    `center_y` FLOAT NOT NULL DEFAULT 0,
